विनीत शर्मा ने सबसे छोटा पुत्र होने के नाते अपने पिता से अतिरिक्त स्नेह प्राप्त किया, लेकिन उनके ईमानदाराना अतिरिक्त श्रमदान की बदौलत ही आज यह समाचार-पत्र इस इलाके में अपना माथा ऊंचा किये खड़ा है। विनीत शर्मा अपने सामथ्र्य के प्रति सदैव विश्वस्त रहे हैं। उनके व्यवहारिक ज्ञान की सूझबूझ ने उन्हें कुशल प्रशासक के रूप में प्रतिष्ठित किया है। दूरदर्शिता उनके व्यक्तित्त्व की वह आंख है, जिसके लिए वे सभी भाइयों में छोटा होने के बावजूद कई बार परिपक्व वजूद दिखाते रहे हैं। 
